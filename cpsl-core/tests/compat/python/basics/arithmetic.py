# Arithmetic — comprehensive coverage
# Basic operations
print(2 + 3)
print(10 - 4)
print(3 * 7)
print(15 / 4)
print(15 // 4)
print(17 % 5)
print(2 ** 10)

# Negative numbers
print(-3 + 1)
print(-10 * -2)
print(-7 // 2)
print(-7 % 2)

# Float arithmetic
print(1.5 + 2.5)
print(3.0 * 2.5)
print(7.5 / 2.5)
print(7.5 // 2.0)

# Operator precedence
print((2 + 3) * 4)
print(2 + 3 * 4)
print(10 - 2 * 3 + 1)
print(2 ** 3 + 1)

# Integer division edge cases
print(7 // 2)
print(-7 // 2)
print(1 // 3)

# Modulo edge cases
print(10 % 3)
print(-10 % 3)
print(10 % -3)

# Power
print(2 ** 0)
print(0 ** 0)
print(3 ** 3)

# Mixed int/float
print(5 + 2.0)
print(10 / 3)
print(10 // 3)
print(10.0 // 3)
