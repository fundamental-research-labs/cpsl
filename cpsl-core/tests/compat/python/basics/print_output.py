# Print & Output — comprehensive coverage
# Basic print
print("hello")
print(42)
print(3.14)

# Multiple args (space-separated)
print(1, 2, 3)
print("a", "b", "c")
print(1, "hello", True)

# Print with type conversions
print(True)
print(False)
print(None)

# Print collections
print([1, 2, 3])
print({"a": 1, "b": 2})
print((1, 2, 3))

# Empty collections
print([])
print({})
print(())

# Nested structures
print([1, [2, 3], [4, [5]]])

# Print booleans in collections
print([True, False, None])

# Single element tuple
print((1,))

# String conversion
print(str(42))
print(str(3.14))
print(str(True))
print(str(False))
print(str(None))
print(str([1, 2]))
